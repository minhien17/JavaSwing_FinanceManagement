    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        Service service = new Service();
        // money
        int check = 1;
        
        Expense expense = new Expense();
        String money = MoneyTextField.getText();
        if(money == "") check = 0;
        try{
            expense.setMoney(Integer.parseInt(money)); 
            
        }catch(Exception ex){
            check = 0;
        }
        if(expense.getMoney()<=0) check = 0;
        
        //date
        Date datechose = DateChooser.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat();
        expense.setDate(dateFormat.format(datechose));
        
        //
        expense.setType(String.valueOf(CategoryComboBox.getSelectedItem()));
        expense.setNote(NoteTextField.getText());
        expense.setId(idngam);
        
        if(check==0){
            JOptionPane.showMessageDialog(EditExpense.this, "Money must be a positive number!", "Invalid", JOptionPane.OK_OPTION);
        }else{
            service.updateExpense(expense);
            update.onClose();
            this.dispose();
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

