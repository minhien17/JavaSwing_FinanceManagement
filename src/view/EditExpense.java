    public EditExpense(int id, updateTransaction update) {
        this.update = update;
        initComponents();
        idngam = id;
        
        String [] categories = {"Food&Drink","Entertainment","Health&Fitness","Donations","Other-Expense","Rental","Houseware","Investment","Personal-Item","Transportation","Family"};
        for(int i=0;i<categories.length;i++){
            CategoryComboBox.addItem(categories[i]);
        }
        
        Expense expense = service.getExpenseById(id);
        MoneyTextField.setText(String.valueOf(expense.getMoney()));
        CategoryComboBox.setSelectedItem(expense.getType());
        NoteTextField.setText(String.valueOf(expense.getNote()));
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date=null;
        try {
            date = dateFormat.parse(expense.getDate());
        } catch (ParseException ex) {
            Logger.getLogger(EditExpense.class.getName()).log(Level.SEVERE, null, ex);
        }
        DateChooser.setDate(date);
    }

