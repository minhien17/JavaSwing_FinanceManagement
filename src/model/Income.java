package model;

import java.util.Date;

/**
 *
 * @author DELL
 */
public class Income {
    private int id;
    private int money;
    private Date date;
    private String note;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    @Override
    public String toString() {
        return "Income{" + "id=" + id + ", money=" + money + ", date=" + date + ", note=" + note + '}';
    }

    
    
    
    
}
